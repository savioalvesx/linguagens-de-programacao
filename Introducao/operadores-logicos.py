""""
    and - Retorna True se ambas as condições forem verdadeira.
    or  - Retorna True se pelo menos uma das condições for verdadeira
    not - Inverte o valor lógico da condição
"""

print(5 > 5 or 5 > 4) #Verdade quando um dos itens é verdade.
print(5 > 5 and 5 > 4) #Verdade quando os dois itens são verdade.
print(not(5 == 5)) #A negação do resultado. 

print(not(False and False and False and True))
print(False and False and False and True)
