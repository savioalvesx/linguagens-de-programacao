print("Programa realiza o calculo do perímetro e a área de um retângulo.\n")

largura = float(input("Informe a largura do retângulo: "))
altura = float(input("Informe a altura do retângulo: "))

perimetro = 2 * (largura + altura)
area = largura * altura

print("O perímetro do retângulo é: ", perimetro)
print("A área do retângulo é: ", area)